/**
 * Retourne les paramètres Leboncoin pour une marque/modèle donnés.
 */
function getLbcParams(marque, modele) {
  let m = (marque || "")
    .toUpperCase()
    .normalize("NFD").replace(/[\u0300-\u036f]/g, "")
    .replace(/\bB\.M\.W\.\b/, "BMW")
    .replace(/\bB\.M\.W\b/, "BMW")
    .replace(/\bMERCEDES BENZ\b/, "MERCEDES-BENZ")
    .replace(/\bCITROEN\b/, "CITROEN")
    .trim();

  let mod = (modele || "")
    .toUpperCase()
    .normalize("NFD").replace(/[\u0300-\u036f]/g, "")
    .replace(/\bSERIE\b/, "SERIE")
    .trim();

  const key = `${m}|||${mod}`;
  const entry = LBC_MAP[key];
  if (!entry) return null;

  return { brand: entry[0], model: entry[1] };
}
